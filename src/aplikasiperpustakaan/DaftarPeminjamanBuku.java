    public void loadTable(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Peminjaman");
        model.addColumn("Buku");
        model.addColumn("Peminjam");
        model.addColumn("Petugas");
        model.addColumn("Tanggal Pinjam");
        model.addColumn("Tanggal Kembali");
    
        try {
            String sql ="SELECT peminjaman.id_peminjaman, books.judul, users.nama, petugas.nama, peminjaman.tgl_pinjam, peminjaman.tgl_kembali FROM `peminjaman`, `books`, `users`, `petugas` WHERE peminjaman.id_book = books.id_book AND peminjaman.id_user = users.id_user AND peminjaman.id_petugas = petugas.id_petugas;";
            java.sql.Connection conn=(Connection)Connect.ConfigDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()){
                model.addRow(new Object[] {res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5), res.getString(6)} );

        }
            peminjamanData.setModel(model);
        }
        catch (Exception e){
            System.out.println(e);
        }
    }
    
