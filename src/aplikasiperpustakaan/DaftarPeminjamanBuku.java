    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        try {
            // TODO add your handling code here:
            DefaultTableModel model = (DefaultTableModel)peminjamanData.getModel();
            this.id_peminjaman = Integer.valueOf(model.getValueAt(peminjamanData.getSelectedRow(), 0).toString());
            
            String sql ="select books.judul, users.nama from books, users, peminjaman where peminjaman.id_peminjaman = '"+this.id_peminjaman+"' and peminjaman.id_book = books.id_book and peminjaman.id_user = users.id_user";
            java.sql.Connection conn=(Connection)Connect.ConfigDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql); 
            
            if(res.next()) {
                this.buku = res.getString(1);
                this.peminjam = res.getString(2);
            }
            
            PanelPengembalian panelPengembalian = new PanelPengembalian();
            
            panelPengembalian.txtidpeminjaman.setText(Integer.toString(this.id_peminjaman));
            panelPengembalian.txtpeminjam.setText(this.peminjam);
            panelPengembalian.txtbuku.setText(this.buku);
            
            panelPengembalian.setDefaultCloseOperation(panelPengembalian.DISPOSE_ON_CLOSE);
            panelPengembalian.setVisible(true);
            
        } catch (SQLException ex) {
            Logger.getLogger(DaftarPeminjamanBuku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

